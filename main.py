# Запрос строки у пользователя
строка = input("Введите строку на русском языке: ")

# Инициализация переменных для подсчёта гласных и согласных
гласные = 0
согласные = 0

# Список гласных букв
гласные_буквы = "аеёиоуыэюяАЕЁИОУЫЭЮЯ"

# Подсчёт гласных и согласных
for символ in строка:
    if символ.isalpha():  # Проверка, является ли символ буквой
        if символ in гласные_буквы:
            гласные += 1
        else:
            согласные += 1

# Вывод результата
print(f"Длина строки: {len(строка)}")
print(f"Количество гласных: {гласные}")
print(f"Количество согласных: {согласные}")
